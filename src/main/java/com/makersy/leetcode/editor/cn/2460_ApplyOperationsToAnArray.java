    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int[] applyOperations(int[] nums) {
            int idx = 0;
            int len = nums.length;
            int[] res = new int[len];
            for (int i = 0; i < len; i++) {
                if (i < len - 1 && nums[i] == nums[i + 1] && nums[i] != 0) {
                    nums[i] <<= 1;
                    res[idx++] = nums[i];
                    i++;
                }else if (nums[i] != 0) {
                    res[idx++] = nums[i];
                }
            }
            return res;
        }
    }
